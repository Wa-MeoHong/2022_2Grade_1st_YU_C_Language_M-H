#pragma warning(disable: 4996)							// scanf오류 안뜨게 하는 pragma정의 
#include <stdio.h>
#define time_N 60										// 시간 계산 상수(60초 = 1분, 60분 = 1시간)

int main(void)
{
	int origin_sec;										// 변환할 시간 ( 단위 : 초 )
	int Hour, Min, Sec;									// 변환된 시간 ( 시간, 분, 초)

	// 시간 입력
	printf("변환할 초를 입력하세요 : ");			
	scanf("%d", &origin_sec);

	// 입력된 시간 변환
	Hour = origin_sec / (time_N * time_N);				// 시간 = 초/3600
	Min = (origin_sec / time_N) - (Hour * time_N);		// 분 = 초/60 - Hour*60
	Sec = origin_sec % time_N;							// 초 = 원래 초 % 60 ( 60을 나눈 후 나머지 ) 

	// 출력
	printf("%2d 시간 %2d 분 %2d 초\n", Hour, Min, Sec); 
	return 0;

}